public class Modelo {
    private String name;
    private String last;
    
    public Modelo(){
        name = "";
        last = "";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLast() {
        return last;
    }

    public void setLast(String last) {
        this.last = last;
    }
    
}
